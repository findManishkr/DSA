class Solution {
public:
    int maxArea(vector<int>& height) {
        int n = height.size();
        int maxArea = 0;

        int i = 0, j = n - 1;

        while (i < j) {
            int H = min(height[i], height[j]);
            int W = (j - i);

            maxArea = max(maxArea, W * H);

            // move pointers
            if (height[i] < height[j])
                i++;
            else if (height[j] < height[i])
                j--;
            else
                i++, j--;
        }

        return maxArea;
    }
};


